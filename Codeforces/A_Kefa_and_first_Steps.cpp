#include <bits/stdc++.h>
using namespace std;

#define ll long long
#define r(i,n) for(ll i=0; i<n; i++)
#define trvs(a) a.begin(), a.end()
#define v vector
#define p pair
#define m unordered_map
#define INF 1e9

ll gcd(ll a, ll b){
    if( b > a ) swap(a, b);
    return b == 0 ? a : gcd(b, a % b);
}

ll lcm(ll a, ll b ){
    return abs(a * b) / gcd(a, b);
}

ll og2(ll x){
    return (64 - __builtin_clzll(x) - 1);
}

void solve() {
    int n;
    ll a[100001];
    cin>>n;
    r(i,n){
        cin>>a[i];
    }
    ll ans = 0,c = 1;
    for(int i=1; i<n; i++){
        if(a[i] >= a[i-1]){
            c++;
        }
        else{
            ans = max(ans,c);
            c = 1;
        }
    }
    cout<<max(ans,c)<<endl;
}


int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    int t = 1;
    while (t--) {
        solve();
    }
    return 0;
}