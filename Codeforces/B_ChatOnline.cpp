#include<bits/stdc++.h>
using namespace std;
#define ll long long

int main(){
    ios::sync_with_stdio(0);
    cin.tie(0);
    ll p, q, l, r, c=0;
    bitset<2010> m(0);
    cin>>p>>q>>r>>l;
    vector<pair <int,int>> a(p);
    vector<pair <int,int>> b(q);
    for(int i=0; i<p; i++){
        int x,y;
        cin>>x>>y;
        a[i].first = x;
        a[i].second = y;
        for(int j =x; j<=y; j++){
            m[j] = 1;
        }
    }

    for(int i=0; i<q; i++){
        int x,y;
        cin>>x>>y;
        b[i].first = x;
        b[i].second = y;
    }
    
        for(int i=r; i<=l; i++){
            bool f = false;
            for(int j=0; j<q && !f; j++){
                for(int k = b[j].first + i; k <=b[j].second + i; k++ ){
                    if( m[k] ) {
                        f = true;
                        break;
                    }
                }
                if(f) c++;
        }
    }
    cout<<c<<endl;
}