#include <bits/stdc++.h>
using namespace std;

#define ll long long
#define r(i,n) for(ll i=0; i<n; i++)
#define trvs(a) a.begin(), a.end()
#define v vector
#define p pair
#define m unordered_map
#define INF 1e9

ll gcd(ll a, ll b){
    if( b > a ) swap(a, b);
    return b == 0 ? a : gcd(b, a % b);
}

ll lcm(ll a, ll b ){
    return abs(a * b) / gcd(a, b);
}

ll og2(ll x){
    return (64 - __builtin_clzll(x) - 1);
}

int ans(string s, int x) {
    int p1 = x - 1;
    while(p1 >= 0 && s[p1] == '.') {
        p1--;
    }
    int an = p1 + 2;
    p1 = x + 1;
    while(p1 < s.size() && s[p1] == '.') {
        p1++;
    }
    an = min(an, (int)s.size() - p1 + 1);
    return an;
}
 
void solve()
{
    ll n,x;
    cin >> n >> x;
    x--;
    string s;
    cin >> s;
    int a1 = 0, a2 = 0;
    {
        string t = s;
        int p1 = x + 1;
        while(p1 < n && s[p1] == '#') {
            p1++;
        }
        if(p1 != n) {
            t[p1] = '#';
            a1 = ans(t,x);
        }
    }
    {
        string t = s;
        int p1 = x-1;
        while(p1 >= 0 && s[p1] == '#') p1--;
        if(p1 != -1) {
            t[p1] = '#';
            a2 = ans(t,x);
        }
    }
 
    cout << max(a1,a2) << "\n";
}


int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(0);
    int t;
    cin >> t;
    while (t--) {
        solve();
    }
    return 0;
}